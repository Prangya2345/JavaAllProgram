package PracticeAllProject;

public class FindOddAndEvenNumber {

	
		public static void main(String[] args) {
	        int number = 8;

	        if (number % 2 == 0) {
	            System.out.println(number + " is Even.");
	        } else {
	            System.out.println(number + " is Odd.");
	        }
	    }

	}


